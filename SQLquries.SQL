CREATE TABLE EMP1 (
 EMPNO               int NOT NULL PRIMARY KEY,
 ENAME               VARCHAR(10) NOT NULL ,
GENDER CHAR(1) DEFAULT 'F',
EMAIL CHAR(20) NOT NULL UNIQUE,
 JOB                 VARCHAR(9) CHECK(JOB IN ('CLERK','SALESMAN','MANAGER','PRESIDENT','ANALYST')),
 MGR                 int REFERENCES EMP1(EMPNO),
 HIREDATE            DATE CHECK (HIREDATE >'2021-1-1'),
 SAL                 int CHECK( SAL>500),
 COMM                int CHECK(COMM>100),
 DEPTNO              int
);

CREATE TABLE EMPLOYEE_CONS (
 EMPNO	int	CONSTRAINT EMPNO_CON NOT NULL PRIMARY KEY,
 ENAME	VARCHAR(10) 	CONSTRAINT ENAME_CON	NOT NULL ,
GENDER 	CHAR(1) 	DEFAULT 'F',
EMAIL 	CHAR(20) 	CONSTRAINT 	EMAIL_CON NOT NULL UNIQUE,
 JOB                 	VARCHAR(9) 	CONSTRAINT 	JOB_CON CHECK(JOB IN ('CLERK','SALESMAN','MANAGER','PRESIDENT','ANALYST')),
 MGR               	int 	CONSTRAINT	MGR_CON REFERENCES EMP1(EMPNO),
 HIREDATE	DATE  	CONSTRAINT	HIREDATE_CON CHECK (HIREDATE >'2021-1-1'),
 SAL                 	int	CONSTRAINT	SAL_CON	CHECK( SAL>500),
 COMM                int	CONSTRAINT	COMM_CON 	CHECK(COMM<SAL),
 DEPTNO              int	CONSTRAINT DEPTNO_CON REFERENCES DEPARTMENT(DPNO)
);

